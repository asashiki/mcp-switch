export const dict: Record<Lang, Record<string, string>> = { zh, en, ja };

/** Read the active language outside React (api client, pure formatter helpers). */
export function currentLang(): Lang {
  try {
    const saved = localStorage.getItem("mcp-switch.lang");
    if (saved === "zh" || saved === "en" || saved === "ja") return saved;
    const nav = (navigator.language || "en").toLowerCase();
    if (nav.startsWith("zh")) return "zh";
    if (nav.startsWith("ja")) return "ja";
  } catch { /* SSR / no storage */ }
  return "en";
}

/** BCP-47 tag for Intl / toLocale* formatting, derived from the active language. */
export function localeTag(lang: Lang = currentLang()): string {
  return lang === "zh" ? "zh-CN" : lang === "ja" ? "ja-JP" : "en-US";
}

/** Translate outside of React (no interpolation). */
export function tStatic(key: string, lang: Lang = currentLang()): string {
  return dict[lang][key] ?? dict.en[key] ?? key;
}
